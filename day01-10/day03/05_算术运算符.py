a = 5
b = 3
print(a + b)        # 求和
print(a - b)        # 求差
print(a * b)        # 求积
print(a / b)        # 求商
print(a ** b)       # 求幂
print(a // b)       # 求整除的商
print(a % b)        # 求余

#【注1】算术运算符可以用于计算变量，也能计算常量
#【注2】python当中算术运算符可以连续运算
#【注3】算术运算符拥有运算优先级，同级别从左向右运算，**最高，* / // % 平级  + - 平级
# 优先级 ** > * / // % > +-
print(1 + 3 + 5 * 6 / 7 ** 2)
