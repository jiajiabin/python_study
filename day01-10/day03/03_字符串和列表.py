# 列表
# 列表用于顺序存储大量的数据，类似于很多语言中的数组，但是列表存储的数据没有类型限制，大多数语言中数组只能存储同一类型的数据。
# 一个列表可以理解为多个变量。
ls = [1, 2, 3, 4, 5]
ls2 = [1, 2.5, True, "hello world!"]
# ls[0], ls[1], ls[2]...ls[4]是列表的5个元素，每个元素都可认为是一个变量。
print(ls[0], ls[1], ls[2], ls[3] + 1)
print(ls2[0], ls2[1], ls2[2])


