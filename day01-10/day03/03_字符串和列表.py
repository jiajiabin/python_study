# 字符串
# 字符串就是一串字符。在Python当中，1至n个连续的字符都可叫做字符串
# 在很多语言当中一个字符就叫做字符，两个以上的字符才叫做字符串

# 其他语言中单引号用于括字符，双引号括字符串。
# python中因为没有字符类型，单引号和双引号均可表示字符串
a = "abc"
b = 'abc'
c: str
c = 'a'
print(a, b, c)
