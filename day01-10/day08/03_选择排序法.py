# 选择排序法的逻辑(从小到大为例)

# [2, 3, 1, 5, 4]
# [(2), (3), 1, 5, 4] -> [2, 3, 1, 5, 4] -> [(2), 3, (1), 5, 4] -> [1, 3, 2, 5, 4] ->  [(1), 3, 2, (5), 4]
#                   -> [1, 3, 2, 5, 4] -> [(1), 3, 2, 5, (4)] - >  [1, 3, 2, 5, 4]
# [1, (3), (2), 5, 4] -> [1, 2, 3, 5, 4] -> [1, (2), 3, (5), 4] -> [1, 2, 3, 5, 4] -> [1, (2), 3, 5, (4)] -> [1, 2, 3, 5, 4]
# [1, 2, (3), (5), 4] -> [1, 2, 3, 5, 4] -> [1, 2, (3), 5, (4)] -> [1, 2, 3, 5, 4]
# [1, 2, 3, (5), (4)] -> [1, 2, 3, 4, 5]

def select_sort(ls:list):
    for i in range(0, len(ls) - 1):
        for j in range(i + 1, len(ls)):
            if ls[i] > ls[j]:
                ls[i], ls[j] = ls[j], ls[i]

ls = [2, 3, 1, 5, 4]
select_sort(ls)
print(ls)
